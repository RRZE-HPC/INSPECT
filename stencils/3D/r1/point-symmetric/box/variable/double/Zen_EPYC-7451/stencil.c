double a[M][N][P];
double b[M][N][P];
double W[14][M][N][P];

for(long k=1; k < M-1; ++k){
for(long j=1; j < N-1; ++j){
for(long i=1; i < P-1; ++i){
b[k][j][i] = W[0][k][j][i] * a[k][j][i]
+ W[1][k][j][i] * (a[k][j][i-1] + a[k][j][i+1])
+ W[2][k][j][i] * (a[k][j-1][i] + a[k][j+1][i])
+ W[3][k][j][i] * (a[k-1][j][i] + a[k+1][j][i])
+ W[4][k][j][i] * (a[k][j-1][i-1] + a[k][j+1][i+1])
+ W[5][k][j][i] * (a[k-1][j][i-1] + a[k+1][j][i+1])
+ W[6][k][j][i] * (a[k+1][j][i-1] + a[k-1][j][i+1])
+ W[7][k][j][i] * (a[k][j+1][i-1] + a[k][j-1][i+1])
+ W[8][k][j][i] * (a[k-1][j-1][i] + a[k+1][j+1][i])
+ W[9][k][j][i] * (a[k+1][j-1][i] + a[k-1][j+1][i])
+ W[10][k][j][i] * (a[k-1][j-1][i-1] + a[k+1][j+1][i+1])
+ W[11][k][j][i] * (a[k+1][j-1][i-1] + a[k-1][j+1][i+1])
+ W[12][k][j][i] * (a[k-1][j+1][i-1] + a[k+1][j-1][i+1])
+ W[13][k][j][i] * (a[k+1][j+1][i-1] + a[k-1][j-1][i+1])
;
}
}
}
